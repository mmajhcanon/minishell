#include "libft.h"

char		*ft_strcat(char *s1, char *s2)
{
	int		j;
	int		i;

	j = 0;
	i = -1;
	while (s1[j])
		j++;
	while (s2[++i])
		s1[j++] = s2[i];
	s1[j] = 0;
	return (s1);
}
